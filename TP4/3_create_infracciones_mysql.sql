CREATE TABLE IF NOT EXISTS `provincia` (
    `idprovincia` SMALLINT NOT NULL,
    `provincia` VARCHAR(32) NOT NULL,
    CONSTRAINT `provincia_pkey` PRIMARY KEY (`idprovincia`)
);

CREATE TABLE IF NOT EXISTS `valuacion` (
    `idmodelo` INT NOT NULL,
    `anio` SMALLINT NOT NULL,
    `valor` INT NOT NULL,
    CONSTRAINT `valuacion_pkey` PRIMARY KEY (`idmodelo`, `anio`)
);

CREATE TABLE IF NOT EXISTS `localidad` (
    `codpostal` SMALLINT NOT NULL,
    `localidad` VARCHAR(32) NOT NULL,
    `idprovincia` SMALLINT NOT NULL,
    CONSTRAINT `localidad_pkey` PRIMARY KEY (`codpostal`),
    FOREIGN KEY (`idprovincia`) REFERENCES `provincia`(`idprovincia`)
);

CREATE TABLE IF NOT EXISTS `persona` (
    `dni` INT NOT NULL,
    `fechanac` DATE NOT NULL,
    `nombre` VARCHAR(40) NOT NULL,
    `sexo` CHAR(1) NOT NULL,
    `domicilio` VARCHAR(40),
    `codpostal` SMALLINT NOT NULL,
    CONSTRAINT `persona_pkey` PRIMARY KEY (`dni`),
    FOREIGN KEY (`codpostal`) REFERENCES `localidad`(`codpostal`)
);

CREATE TABLE IF NOT EXISTS `marca` (
    `idmarca` SMALLINT NOT NULL,
    `marca` VARCHAR(20) NOT NULL,
    CONSTRAINT `marca_pkey` PRIMARY KEY (`idmarca`)
);

CREATE TABLE IF NOT EXISTS `modelo` (
    `idmodelo` INT NOT NULL,
    `modelo` VARCHAR(32) NOT NULL,
    `idmarca` SMALLINT NOT NULL,
    `importado` TINYINT(1) NOT NULL,
    CONSTRAINT `modelo_pkey` PRIMARY KEY (`idmodelo`),
    FOREIGN KEY (`idmarca`) REFERENCES `marca`(`idmarca`)
);

CREATE TABLE IF NOT EXISTS `vehiculo` (
    `dni` INT NOT NULL,
    `idmodelo` INT NOT NULL,
    `anio` SMALLINT NOT NULL,
    `patente` CHAR(9) NOT NULL,
    CONSTRAINT `vehiculo_pkey` PRIMARY KEY (`patente`),
    FOREIGN KEY (`idmodelo`) REFERENCES `modelo`(`idmodelo`)
);

CREATE TABLE IF NOT EXISTS `tipoinfraccion` (
    `tipoinfraccion` SMALLINT NOT NULL,
    `descripcion` VARCHAR(40) NOT NULL,
    `importe` INT NOT NULL,
    CONSTRAINT `tipoinfraccion_pkey` PRIMARY KEY (`tipoinfraccion`)
);

CREATE TABLE IF NOT EXISTS `infraccion` (
    `idinfraccion` INT NOT NULL,
    `dni` INT NOT NULL,
    `patente` CHAR(9) NOT NULL,
    `tipoinfraccion` SMALLINT NOT NULL,
    `fecha` DATE NOT NULL,
    `direccion` VARCHAR(30) NOT NULL,
    `codpostal` SMALLINT NOT NULL,
    `pagada` TINYINT(1) NOT NULL,
    CONSTRAINT `infraccion_pkey` PRIMARY KEY (`idinfraccion`),
    FOREIGN KEY (`dni`) REFERENCES `persona`(`dni`),
    FOREIGN KEY (`patente`) REFERENCES `vehiculo`(`patente`),
    FOREIGN KEY (`tipoinfraccion`) REFERENCES `tipoinfraccion`(`tipoinfraccion`),
    FOREIGN KEY (`codpostal`) REFERENCES `localidad`(`codpostal`)
);

